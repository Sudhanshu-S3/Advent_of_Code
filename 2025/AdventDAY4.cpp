// ========================== PART 1 ======================================

void solve1()
{
    vector<string> input;
    string line;
    int count = 0;
    
    while(getline(cin, line))
    {
        input.push_back(line);
    }
    
    int col = input.size();
    
    for(int i = 0; i < col; i++)
    {
        for(int j = 0; j < (int)input[i].length(); j++)
        {
         
            if(input[i][j] != '@')
                continue;
                
            int t = 0;
            
            if(i > 0 && input[i-1][j] == '@')
            {
                t++;
            }

            if(i < col-1 && input[i+1][j] == '@')
            {
                t++;
            }
            
            if(i > 0 && j > 0 && input[i-1][j-1] == '@')
            {
                t++;
            }
            
            if(i < col-1 && j > 0 && input[i+1][j-1] == '@')
            {
                t++;
            }
            
            if(j > 0 && input[i][j-1] == '@')
            {
                t++;
            }
            
            if(j < (int)input[i].length()-1 && input[i][j+1] == '@')
            {
                t++;
            }
            
            if(i > 0 && j < (int)input[i].length()-1 && input[i-1][j+1] == '@')
            {
                t++;
            }
            
            if(i < col-1 && j < (int)input[i].length()-1 && input[i+1][j+1] == '@')
            {
                t++;
            }
            
            if(t < 4)
            {
                count++;
            }
        }
    }
    cout << count << endl;
}

// ========================== PART 2 ======================================

void solve2()
{
    vector<string> input;
    string line;
    int totalRemoved = 0;
    
    while(getline(cin, line))
    {
        input.push_back(line);
    }
    
    int col = input.size();
    bool changed = true;
    
    while(changed)
    {
        changed = false;
        vector<pair<int, int>> toRemove;
        
        for(int i = 0; i < col; i++)
        {
            for(int j = 0; j < (int)input[i].length(); j++)
            {
            
                if(input[i][j] != '@')
                    continue;
                    
                int t = 0;
                
                if(i > 0 && input[i-1][j] == '@')
                {
                    t++;
                }
                
                if(i < col-1 && input[i+1][j] == '@')
                {
                    t++;
                }
               
                if(i > 0 && j > 0 && input[i-1][j-1] == '@')
                {
                    t++;
                }
                
                if(i < col-1 && j > 0 && input[i+1][j-1] == '@')
                {
                    t++;
                }
                
                if(j > 0 && input[i][j-1] == '@')
                {
                    t++;
                }
                
                if(j < (int)input[i].length()-1 && input[i][j+1] == '@')
                {
                    t++;
                }
                
                if(i > 0 && j < (int)input[i].length()-1 && input[i-1][j+1] == '@')
                {
                    t++;
                }

                if(i < col-1 && j < (int)input[i].length()-1 && input[i+1][j+1] == '@')
                {
                    t++;
                }
                
                
                if(t < 4)
                {
                    toRemove.push_back({i, j});
                }
            }
        }

        if(!toRemove.empty())
        {
            changed = true;
            for(auto p : toRemove)
            {
                input[p.first][p.second] = '.';
                totalRemoved++;
            }
        }
    }
    
    cout << totalRemoved << endl;
}

int main()
{
    solve2();
    return 0;
}

